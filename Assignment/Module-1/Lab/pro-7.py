#Practical Example 4: How to check the type of a variable dynamically using type().

name = "Dhvani Pancholi"
age = 22
height = 5.7
graduate = True
complex_num = 45+6j

print("name",type(name))
print("age",type(age))
print("height",type(height))
print("graduate",type(graduate))
print("complex number",type(complex_num))